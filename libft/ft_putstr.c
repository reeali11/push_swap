#include "libft.h"

void	ft_putstr(char *str, int *len)
{
	size_t	i;

	i = 0;
	if (!str)
	{
		write(1, "(null)", 6);
		(*len) = (*len) + 6;
		return ;
	}
	while (str[i] != '\0')
	{
		ft_putchar (str[i], len);
		i++;
	}
}
